import static org.nnsoft.guice.gache.Cached.DEFAULT_ID;
import static java.lang.annotation.ElementType.METHOD;
import static java.lang.annotation.RetentionPolicy.RUNTIME;

import java.lang.annotation.Documented;
import java.lang.annotation.Inherited;
import java.lang.annotation.Retention;
import java.lang.annotation.Target;

/**
 * Identifies the methods that flush the cache when executed.
 */
@Inherited
@Documented
@Retention( RUNTIME )
@Target( METHOD )
public @interface Flush
{

    /**
     * Ids of the flushing model to be used to flush the cache.
     *
     * @return flushing model Ids
     */
    String[] value() default { DEFAULT_ID };

}
